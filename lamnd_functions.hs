-- A "Lamnda" is for writing a function whitout giving it a name
-- So a lamnda is a function, not a type itself
-- Lamnda is denoted with '\' in haskell 

-- Lamndas are specially usefull when there needs a small function only once, specially as an argument
-- to another function.

-- e.g with map
map (\x -> x + 1) [1, 2, 3]
-- This should result in: [2, 3, 4]


